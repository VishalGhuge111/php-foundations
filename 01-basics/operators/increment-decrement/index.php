<?php

// increment and decrement

$num1 = 5;
echo "<p>num1: $num1</p>";
$num1++;
echo "<p>num1 after num1++: $num1</p>";

$num2 = 5;
echo "<p>num2: $num2</p>";
$num2--;
echo "<p>num2 after num2--: $num2</p>";

$num3 = 5;
echo "<p>num3: $num3</p>";
+$num3;
echo "<p>num3 after ++num3: $num3</p>";

$num4 = 5;
echo "<p>num4: $num4</p>";
--$num4;
echo "<p>num4 after --num4: $num4</p>";
